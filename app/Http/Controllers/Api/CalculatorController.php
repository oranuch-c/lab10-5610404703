     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //width, length, hight in centimeter(cm.)
        $w = $request->width;
        $l = $request->length;
        $h = $request->height;
        $cbm = $w*$l*$h/1000000;
        $weight = $cbm*150;
        $q = $request->priceType;
        $total = ceil($q*$cbm);

        return [
          'success'=>true,
          'data'=> [
            'weight' => $weight,
            'total' => $total
          ]
        ];

    }

